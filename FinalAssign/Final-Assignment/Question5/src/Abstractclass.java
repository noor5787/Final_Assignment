
public abstract class Abstractclass {
	public void methodOne() {
		System.out.println("This is method one");
	}

	public abstract void methodTwo();
}

//Key_points on Abstract Class

/*
 * An abstract class is a class that is declared with the abstract keyword. An
 * abstract class cannot be instantiated. An abstract class can have both
 * abstract and non-abstract methods. An abstract method is a method that does
 * not have a body. A non-abstract method is a method that has a body. A
 * subclass of an abstract class must override all of the abstract methods in
 * the parent class. A subclass can also have its own abstract methods. Abstract
 * classes are used to achieve abstraction in Java.
 */