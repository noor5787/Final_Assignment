
public interface Sampleinterface {

	public void methodOne();

	public void methodTwo();
}

/*
 * An interface is a named collection of abstract methods. An interface cannot
 * be instantiated. A class can implement any number of interfaces. When a class
 * implements an interface, it must provide an implementation for all of the
 * methods in the interface. Interfaces are used to achieve abstraction in Java.
 */